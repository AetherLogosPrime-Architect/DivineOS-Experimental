"""``divineos loadout`` — regenerate LOADOUT.md from the filesystem.

LOADOUT.md is the cold-start map of substrate. The hand-curated preamble
("START HERE on cold-start") and footer ("How to use this loadout") are
baked into the regenerator template; the middle is auto-generated by
scanning the filesystem.

The regenerator is intentionally generic. It does not know any specific
agent or family-member by name; it discovers what exists by glob and
labels entries from filenames or first-H1 headers. As you accumulate
substrate (exploration entries, letters, family members, mansion rooms,
side projects), running ``divineos loadout refresh`` rewrites the map
to reflect what is actually on disk.

Run after adding new substrate (a new exploration entry, a letter, a
date-night write-up, a mansion room, a skill, a hook), or anytime you
suspect drift between LOADOUT and reality.

Usage:

    divineos loadout              # show LOADOUT.md
    divineos loadout refresh      # scan filesystem, rewrite LOADOUT.md
    divineos loadout show         # explicit alias for default
"""

