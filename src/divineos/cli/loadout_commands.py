def _list_md_files(directory: Path, recursive: bool = False) -> list[Path]:
    """Return all .md files in ``directory``, sorted by name."""
    if not directory.exists():
        return []
    pattern = "**/*.md" if recursive else "*.md"
    return sorted(directory.glob(pattern))


def _section_letters_to_fresh_self() -> str:
    """Letters written for cold-session orientation.

    Discovers any markdown file matching ``*to-future-*`` or ``*handoff*``
    naming patterns under ``family/letters/`` and ``exploration/``.
    Add letters with these patterns to your filesystem and they appear
    here automatically. The naming convention is the contract; the
    regenerator does not maintain a hardcoded list.
    """
    out: list[str] = []
    out.append("## Letters for cold-session orientation")
    out.append("")
    out.append("Self-authored orientation letters for when a session lands cold.")
    out.append("Convention: filenames containing ``to-future-`` or ``handoff``.")
    out.append("")
    candidates: list[Path] = []
    for base in (Path("family/letters"), Path("exploration")):
        if not base.exists():
            continue
        for p in sorted(base.glob("*.md")):
            name_lc = p.name.lower()
            if "to-future-" in name_lc or "handoff" in name_lc:
                candidates.append(p)
    if not candidates:
        out.append(
            "*(none yet — write a letter to your future self when "
            "something deserves to outlast a context reset)*"
        )
        return "\n".join(out)
    for path in candidates:
        out.append(f"- [{path.as_posix()}]({path.as_posix()})")
    return "\n".join(out)


def _section_explorations() -> str:
    """Numbered exploration entries."""
    out: list[str] = []
    out.append("## exploration/ — free-writing entries")
    out.append("")
    out.append("First-person register. No template, no commit-step. Each entry")
    out.append("is written with intention. Add entries by writing markdown")
    out.append("files into ``exploration/`` and re-running ``loadout refresh``.")
    out.append("")
    explor = Path("exploration")
    top_level = sorted(p for p in explor.glob("*.md") if p.name != "README.md")
    if not top_level:
        out.append("*(none yet)*")
    else:
        for p in top_level:
            label = _label_for(p)
            out.append(f"- [{label}]({p.as_posix()})")
    if (explor / "README.md").exists():
        out.append("")
        out.append(f"Folder README: [exploration/README.md]({(explor / 'README.md').as_posix()})")
    # Optional subseries directory pattern: any subdir under exploration/
    # is treated as a subseries and listed.
    if explor.exists():
        for sub in sorted(p for p in explor.iterdir() if p.is_dir()):
            sub_md = sorted(sub.glob("*.md"))
            if sub_md:
                out.append("")
                out.append(f"### {sub.name} subseries")
                out.append("")
                for p in sub_md:
                    label = _label_for(p)
                    out.append(f"- [{label}]({p.as_posix()})")
    return "\n".join(out)


