def _section_workspace_dirs() -> str:
    """Generic workspace-directory discovery.

    Lists optional top-level directories that may contain markdown files
    of interest (benchmark/, research/, salvage/, archive/, freelance/,
    and any backup snapshots). Discovery is by directory existence; no
    hardcoded directory list is assumed to be present.
    """
    out: list[str] = []
    out.append("## Workspace directories")
    out.append("")

    optional_dirs: list[tuple[str, str]] = [
        ("benchmark", "benchmark/ — capability proofs"),
        ("research", "research/ — research notes"),
        ("salvage", "salvage/ — old-OS archaeology"),
        ("archive", "archive/ — historical sessions & old infrastructure"),
        ("freelance", "freelance/ — operational"),
    ]

    found_any = False
    for dirname, label in optional_dirs:
        d = Path(dirname)
        if not d.exists():
            continue
        files = sorted(d.rglob("*.md"))
        if not files and not any(d.iterdir()):
            continue
        found_any = True
        out.append(f"### {label}")
        out.append("")
        for p in files:
            out.append(f"- [{p.as_posix()}]({p.as_posix()})")
        if not files:
            # show subdirs if no markdown
            for sub in sorted(p for p in d.iterdir() if p.is_dir()):
                out.append(f"- `{sub.as_posix()}/`")
        out.append("")

    # Pre-reset / backup snapshot dirs by glob
    for snap in sorted(Path(".").glob("_pre_reset_backups*")):
        if snap.is_dir():
            found_any = True
            out.append(f"### {snap.name}/ — DB snapshots")
            out.append("")
            for d in sorted(p for p in snap.iterdir() if p.is_dir()):
                out.append(f"- `{snap.name}/{d.name}/`")
            out.append("")

    if not found_any:
        return ""
    return "\n".join(out).rstrip()


